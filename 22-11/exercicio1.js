// FOR... OF
// Geralmente usado quando eu quero varrer um array inteiro.
const numeros = [1, 15, 13, 22, 39, 45];
for (let item of numeros) {
    console.log(item);
}
// Geralmente usa quando não se sabe quantas repetições serão
while (numeros <= 7){
    console.log(item);
    numeros++;
}

